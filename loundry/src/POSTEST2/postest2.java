        godar = jCBgodar.getSelectedItem().toString();
        pekerjaan = jTFpekerjaan.getText();
        String kewarganegaraan=null;
            if (jRBwna.isSelected())
            {kewarganegaraan = jRBwni.getText();}
            else if (jRBwni.isSelected())
            {kewarganegaraan = jRBwna.getText();}
            
       
       output x = new output(Nama, ttl, tl, JK, Alamat, RT, RW, desa, Kec, kota, agama, status, godar, pekerjaan, kewarganegaraan);
        x.setVisible(true);
