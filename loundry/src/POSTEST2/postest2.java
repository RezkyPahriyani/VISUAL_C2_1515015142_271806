    private void jBchooseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBchooseActionPerformed
        // TODO add your handling code here:
        JFileChooser OPEN = new JFileChooser();
        int returnVal = OPEN.showOpenDialog(null);
        
        File filegambar = null;
        if (returnVal == JFileChooser.APPROVE_OPTION){
            filegambar=OPEN.getSelectedFile();
            String filename = filegambar.getAbsolutePath();
        }
        try {
            BufferedImage gambar=ImageIO.read(filegambar);
            ImageIcon icon=new ImageIcon(gambar.getScaledInstance(180,240,0));
            jLpoto.setIcon(icon);
            output outt =new output(icon);
            outt.setVisible(true);
            
        }
        catch (IOException e){e.getMessage();}
    }//GEN-LAST:event_jBchooseActionPerformed

